    public void loadInitialData() {
        try {
            sells = dao.getAllSells();
            jTableSells.setModel(new MyTableModel(Sell.class,sells,jTableSells));
            jTableItensOfSell.setModel(new MyTableModel(SellItem.class, new LinkedList<SellItem>(), jTableItensOfSell));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Cannot load the sells","Error",JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(frmSells.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void addSellTableListener() {
        jTableSells.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent e) {
                try {
                    Sell selectedSell = sells.get(jTableSells.getSelectedRow());
                    jTableItensOfSell.setModel(new MyTableModel(SellItem.class, selectedSell.getItens(), jTableItensOfSell));
                } catch (Exception ex) {
                    
                }
            }
        });
    }
