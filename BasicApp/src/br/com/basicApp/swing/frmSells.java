    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if(jTableSells.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this,"You must select one sell first !","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        int resp = JOptionPane.showConfirmDialog(this,"Are you sure ? " , "Delete a Sell",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if (resp == JOptionPane.YES_OPTION){
            try {
                dao.removeSell(sells.get(jTableSells.getSelectedRow()).getId());
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this,"Error removing Sell","Error",JOptionPane.ERROR);
            }
            loadInitialData();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed


