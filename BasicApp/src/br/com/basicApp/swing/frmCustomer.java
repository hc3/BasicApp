    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(jTableCustomer.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this,"Select the user to edit !");
            return;
        }
        frmUpdateCustomer dialog = new frmUpdateCustomer(new javax.swing.JFrame(),true,dao,this,customers.get(jTableCustomer.getSelectedRow()));
        dialog.setVisible(true);
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if(jTableCustomer.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this,"Select the Customer to remove !");
            return;
        }

        int result = JOptionPane.showConfirmDialog(this,"Are you sure ?" , "Remove the Current Customer" ,JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE );
        if(result == 0) {
            try{
                dao.removeCustomer(customers.get(jTableCustomer.getSelectedRow()).getId());
            }catch(SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this,"Error removig Customer","Remove Customer",JOptionPane.ERROR_MESSAGE);
            }
        }
        loadInitialData();
    }//GEN-LAST:event_btnRemoveActionPerformed

