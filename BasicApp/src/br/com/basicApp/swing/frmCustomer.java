    public void loadInitialData() {
        try{
          dao = new CustomerDAO();
          customers = dao.getAllCustomers();
          jTableCustomer.setModel(new MyTableModel(Customer.class,customers,jTableCustomer));
        }catch(SQLException ex){
            ex.printStackTrace();
        }
    }
